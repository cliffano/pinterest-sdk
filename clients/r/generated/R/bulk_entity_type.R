# add to utils.R
.parse_BulkEntityType <- function(vals) {
  res <- gsub("^\\[|\\]$", "", "[CAMPAIGN, AD_GROUP, PRODUCT_GROUP, AD, KEYWORD]")
  unlist(strsplit(res, ", "))
}

